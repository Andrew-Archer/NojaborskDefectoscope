package ru.npptmk.bazaTest.defect.model;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;

/**
 *
 * @author razumnov
 */
@Entity
@NamedQueries({
    @NamedQuery(name = "getByName", query = "SELECT s FROM UpdateScript s WHERE "
            + "s.fileName = :fileName"),
    @NamedQuery(name = "getAll", query = "SELECT s FROM UpdateScript s")

})
public class UpdateScript implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @Column(name = "FILE_NAME")
    private String fileName;

    public UpdateScript() {
    }

    public UpdateScript(String fileName) {
        super();
        this.fileName = fileName;
    }

    /**
     * @return the fileName
     */
    public String getFileName() {
        return fileName;
    }

    /**
     * @param fileName the fileName to set
     */
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof UpdateScript)) {
            return false;
        }
        UpdateScript other = (UpdateScript) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "UpdateScript[ name=" + fileName + " ]";
    }

}
