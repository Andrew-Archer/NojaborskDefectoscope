    private final GraficsIcon icon;
    private DefaultTableModel tm;
    private boolean isSetup = true;

    /**
     * Диалог с стаблицей видимости графиков на иконке.
     *
     * @param icon иконка, чьими графиками нужно управлять.
     */
    public GrafsVisibilityDialog(GraficsIcon icon) {
        super((java.awt.Frame) null, false);
        this.icon = icon;
        initComponents();
        tm = (DefaultTableModel) grafsTable.getModel();
        tm.addTableModelListener(this);
        grafsTable.getColumnModel().getColumn(1).setCellRenderer(new CellRenderer());
        HashMap<Integer, Grafic> hm = icon.getGrafs();
        Object[] data = new Object[2];
        for (int i = 0; i < hm.size(); i++) {
            Grafic gr = hm.get(i);
            if (gr != null) {
                data[0] = gr.visible;
                data[1] = gr;
                tm.addRow(data);
            }
        }
        setLocationRelativeTo(null);
        isSetup = false;
    }

    @Override
    public void tableChanged(TableModelEvent e) {
        if (isSetup){
            return;
        }
        int col = e.getColumn();
        int row = e.getFirstRow();
        if (row == -1 || col == -1){
            return;
        }
        Boolean vis = (Boolean) tm.getValueAt(row, col);
        Grafic gr = (Grafic) tm.getValueAt(row, 1);
        gr.visible = vis;
        icon.repaint();
    }

    public class CellRenderer extends JLabel implements TableCellRenderer {

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Grafic gr = (Grafic) value;
            setText(gr.name);
            setForeground(gr.clr);
            setBackground(Color.WHITE);
            setOpaque(true);
            return this;
        }

    }

