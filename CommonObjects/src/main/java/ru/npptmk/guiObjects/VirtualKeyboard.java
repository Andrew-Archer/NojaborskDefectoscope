    public VirtualKeyboard(java.awt.Frame parent) {
        super(parent, true);
        initComponents();
        edVal.addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent e) {
                char digit = e.getKeyChar();
                if(digit == KeyEvent.VK_ENTER){
                    jButton14ActionPerformed(null);
                }
                if(digit == KeyEvent.VK_ESCAPE){
                    setVisible(false);
                }
                if(digit == '.' && edVal.getText().indexOf(',') < 0){
                    addDigit(String.valueOf(','));
                }
                if(!((digit >= '0' && digit <= '9') || (digit == ',' && edVal.getText().indexOf(',') < 0))){
                    e.consume();
                    return;
                }
                if(digit == ','){
                    jButton12.setEnabled(false);
                } else {
                    enableComa();
                }
            }
        });
        edVal.requestFocus();
    }

    /**
     * Ввод числового значения с помощью виртуальной клавиатуры.<br>
     * Это основной метод использования данного объекта. Вызов этого метода
     * приводит к отображению экранной клавиатуры поверх указанного компонента с
     * заданным начальным значением в окне клавиатуры.<br>
     * Метод блокируется до нажатия клавиши "Enter" или закрытия окна
     * клавиатуры. При нажатии на "Enter" - возвращается строка из окна
     * клавиатуры, при закрытии окна - {@code null}.
     *
     * @param cp компонент, относительно которого базируется клавиатура.
     * @param begval начальное значение в окне клавиатуры.
     * @return Строку введенного значения, или {@code null} в случае
     * принудительного закрытия окна клавиатуры. Перед возвратом, десятичная
     * запятая в числе заменяется точкой. Таким образом, возвращенная строка
     * может непосредственно преобразовываться в число с плавающей точкой.
     */
    public String getValue(Component cp, String begval) {
        setLocationRelativeTo(cp);
        prEdVal = begval.trim();
        edVal.setText(prEdVal);
        enableComa();
        edVal.selectAll();
        retVal = null;
        setVisible(true);
        if (retVal != null) {
            return retVal.replaceAll(" ", "");
        }
        return null;
    }

    private void enableComa() {
        if (edVal.getText().contains(",")) {
            jButton12.setEnabled(false);
        } else {
            jButton12.setEnabled(true);
        }
    }

    private void addDigit(String digit) {
        String tx = edVal.getText();
        if (digit == null) {
            // Нажат символ забоя
            if (tx.length() > 0) {
                edVal.setText(tx.substring(0, tx.length() - 1));
                enableComa();
            }
            return;
        }
        if (digit.equals("-")) {
            // Нажат символ смены знака
            if (tx.length() > 0) {
                if (tx.substring(0, 1).equals("-")) {
                    edVal.setText(tx.substring(1));
                } else {
                    edVal.setText("-" + tx);
                }
            } else {
                edVal.setText("-");
            }
            return;
        }
        //Если текст был выделен удаляем.
        if(edVal.getSelectedText() != null && !edVal.getSelectedText().isEmpty()){
            tx = "";
        }
        // Нажат любой другой символ
        edVal.setText(tx + digit);
        enableComa();
    }

