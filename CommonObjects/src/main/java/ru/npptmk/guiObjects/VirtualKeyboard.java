/**
 * Виртуальная клавиатура для ввода числовых значений с помощью мыши или
 * сенсорного экрана.<br>
 * Объект этого класса должен быть создан в приложении и при необходимости ввода
 * числового значения (например при получении фокуса текстовым полем) необходимо
 * вызвать его метод {@code getValue()}. Это вызовет появление виртуальной
 * клавиатуры. Расположение клавиатуры определяется графическим компонентом,
 * переданым в этот метод. Результат ввода возвращается как значение этого
 * метода
 *
 * @author MalginAS
 */
